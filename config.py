"""===== 一些名词解释 =====
1、RMSNorm：基于均方根的归一化方式，让模型在前向传播过程中向量空间的整体尺度始终整体维持在1这个区间浮动，避免尺度失控，从而提升模型训练和推理的稳定性。
  如果不做归一化，前向传播过程中各子层会让激活值的整体尺度逐步远离目标区间值，从而导致反向传播过程中，训练变得难以收敛，甚至出现梯度爆炸。
  均方根公式：RMSNorm(x) = x / RMS(x) * gamma，其中 RMS(x) = sqrt(mean(x²) + eps)。
  本质就是：如果向量空间中整体尺度(量级)远大于1，或者远小于1，那就逐元素除以一个数值来整体做尺度的放大或者缩小，保持向量空间的尺度始终在区间1这个标准附近

2、向量维度：token的向量空间维度，维度越大，token的语义的特征越大，模型语义细节越丰富；

3、自注意力：Decoder-Only核心，基于因果掩码约束，让每个位置都能够从左侧已出现的token中按权重选择并聚合最相关的信息，形成当前的上下文表示，确保远距离上下文可以直接建立联系。
  实际论文实现上，基本都是采用多头自注意力计算，用 向量维度/头数 得到每个头可独立计算的子向量空间维度，不同的头负责学习不同维度的语言特征，最后把结果拼接回完整的向量维度。
  这里说一下，Attention内部再计算完成后，就会把向量恢复为原始的向量维度。

  Block中的多头自注意力计算，本质就是将 输入向量*W_Q、W_K、W_V参数矩阵，得到QKV投影，然后进行Q·K 点积 → 缩放（÷√d） → 因果掩码 → softmax → dropout → 加权 V → 新向量得到融合了当前上下文的新向量激活值。

4、Block层数：每一层都会进行「RMSNorm → 多头自注意力计算 → 残差连接 → RMSNorm → SwiGLU FFN（前馈网络） → 残差连接」计算
  堆叠层数越深，模型对语义的理解会越深，训练效果越好；

5、残差连接：主要是避免模型瞎特么学，各个子层计算的时候，会有一个旁路来保存主干内容（原输入内容），最后子层的计算结果=主干+子层计算，循环往复直至Block结束。

6、FNN：前馈网络，简单来说，前向传播过程中，模型在Block子层的计算最后一环，token的语义特征已经比较丰富了，但是FNN会把向量维度扩大N倍，
  进一步强化token的语义特征并删除一些冗余信息，最后再压缩回原向量空间维度，以获得更好的语义特征表达。

7、token上下文长度：长度越大，模型在预训练阶段能够学到更好的语法、语义特征，微调阶段能够支持更长的对话内容。
  如果微调阶段内容很小，一个batch内，模型会做大量的内容补齐动作，这里也是开销；
  还有，如果模型的上下文很大，模型训练的RT也会越久，因为「QK^T → softmax → 加权求和」的矩阵计算相当消耗资源；

8、dropout：这玩意儿不一定是必须的，看模型的参数大小，如果参数大，训练token多，那么dropout就不一定需要
  训练参数小，以及训练token少的时候，为了避免模型特征固化，降低过拟合风险，dropout是必须的，推理阶段，dropout关闭，所有神经元正常工作；
  本质就是前向传播的训练过程中，随机把 部分 向量维度的激活值特征置为0

9、vocab_size次表大小：BPE分词器在训练后，所表示的词库词表的实际大小；

10、batch_size：一个batch内，模型处理的token数量；这个值是token长下文 * 条数；

11、epochs：训练轮数，预训练阶段主要是学语法，微调阶段主要是训练实际LLM的对话能力，训练的轮数越多，loss值越低，但是也容易过拟合；

12、loss值：loss值是模型的预测结果与实际结果经过交叉熵(Cross-Entropy)算出来的偏差分数，而反向传播中，根据这个偏差分数，模型可以进行复合函数求导算出每一个参数的梯度值，然后作为模型各个参数优化更新的优化建议供优化器使用。
  loss值越低，表示答案越正确；

13、learning_rate：学习率，本质是反向传播过程中优化器在更新向量参数的更新步长大小。
  公式：新权重 = 旧权重 - LR(学习率) × 梯度
  LR值太大，模型容易跳过最优解，LR值太小，训练速度慢同时模型容易卡在局部最优而非全局最优。

14、梯度：本质是 loss 对参数的修改建议。前向传播先计算出loss，然后反向传播再沿着 Block 各子层按相反方向，基于复合函数求导和链式法则，逐层计算影响 loss 值的建议值；
    若同一变量（参数+中间值）有多条路径汇合，梯度会累加。最终，每轮反向传播结束后，会得到最终梯度值。
    向量维度中的每一个参数都对应着一个梯度维度中的元素，以0值为基准点，梯度值越大，表示这个参数要往小调，反之往大调，步长就是梯度值 × LR；
    一个batch的梯度数据是一个参考，数据并不准确，怎么调才是最优解，需要基于LR值，反向传播中优化器逐步尝试；
    说到底，loss值就是预测和结果的实际偏差，梯度给了一个调整建议，而LR基于这份建议来调整优化步长;

15、梯度爆炸：反向传播中梯度值异常增大，导致参数更新幅度失控，导致loss值变得非常大，模型无法收敛。

16、输入向量：有2个语义，第一个是进入Block层之前，表示的是Token embedding+Position embedding；其次同一个Block中上一个子层的输出向量，以及上一个Block层的整体输出向量；
"""

from dataclasses import dataclass


@dataclass
class NovaConfig:
    # ========================
    # Nova的架构超参
    # ========================

    # RTX 4090 D (24GB) — 目标 ~22M 参数（宽而浅，GPU 友好）
    # d_model = 384, n_heads = 6, n_layers = 4, d_ff = 1536
    # max_seq_len = 1024, batch_size = 256
    # 训练预算: 3 亿预训练 tokens + 1 亿 SFT tokens = 4 亿 (Chinchilla 最优)

    # Total = V × D + S × D + L × (2D + 4D² + 3D × F) + D + D × V
    # 其中：
    # V = vocab_size（词表大小）
    # D = d_model（嵌入维度）
    # S = max_seq_len（最大序列长度）
    # L = n_layers（Transformer 层数）
    # F = d_ff（FFN 隐藏维度）

    # 向量维度
    d_model: int = 384

    # 多头注意力的头数，d_model / n_heads 维的子空间
    n_heads: int = 6

    # Decoder-Only Block的堆叠层数
    n_layers: int = 4

    # FNN(前馈网络)维度,建议 d_ff = 4 × d_model
    d_ff: int = 1536

    # 预训练token上下文长度
    pretrain_max_seq_len: int = 128
    # 微调token上下文长度
    finetune_max_seq_len: int = 128
    max_seq_len: int = 128

    # dropout概率,推理时关闭
    dropout: float = 0.1

    # 词表大小,由BPE分词器在扫描训练数据后动态设置
    vocab_size: int = 0

    # ========================
    # 训练配置参数
    # ========================

    # 预训练阶段的 batch_size
    pretrain_batch_size: int = 8

    # 微调阶段的 batch_size
    finetune_batch_size: int = 16
    batch_size: int = 8

    # 预训练轮数
    pretrain_epochs: int = 1
    # 微调轮数
    finetune_epochs: int = 500
    epochs: int = 1

    # 预训练阶段学习率
    pretrain_lr: float = 5e-4
    # 微调阶段学习率
    finetune_lr: float = 3e-4
    learning_rate: float = 5e-4

    # 权重衰减系数,标准是0.01,优化器会让参数往0拉一点,防止过拟合
    weight_decay: float = 0.01

    # 学习率预热步数,训练初期模型参数是随机的，梯度方向不可靠。前 100 步学习率从 0 线性增长到learning_rate，给模型一个"热身"阶段，避免一开始步子太大走偏
    warmup_steps: int = 100

    # 梯度裁剪阈值,反向传播时，如果梯度的 L2 范数超过 1.0，就按比例缩小到 1.0。防止个别 batch产生异常大的梯度导致参数剧烈震荡（梯度爆炸）
    grad_clip: float = 1.0

    # ========================
    # 生成（推理）配置参数
    # ========================

    # 采样温度（Temperature）。
    #     控制生成文本的"创造力"。模型输出的 logits 在 softmax 前会除以 temperature：
    #     - temperature < 1：概率分布变尖锐，模型倾向选概率最高的字（更保守、更确定）
    #     - temperature = 1：保持原始概率分布
    #     - temperature > 1：概率分布变平坦，模型更愿意尝试低概率的字（更随机、更有创意）
    #     0.8 略偏保守，适合问答场景追求准确性。
    temperature: float = 0.8

    # Top-k 采样的 k 值。
    # 生成下一个字时，只从概率最高的 k=20 个候选字中采样，其余候选字的概率直接
    # 设为 0。这样既保留了一定的多样性（不是贪心地只选第 1 名），又避免了从概率
    # 极低的"噪声"字中采样导致输出乱码
    top_k: int = 20
